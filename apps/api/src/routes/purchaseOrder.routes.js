router.post('/', requireRole('OWNER','ADMIN','ACCOUNTANT','WAREHOUSE'), async (req, res) => {
  try {
    const { supplierId, expectedDate, currency, exchangeRate, lines, notes } = req.body;
    if (!supplierId || !lines?.length) return res.status(400).json({ error: 'Supplier and line items are required' });
    const supplier = await prisma.supplier.findFirst({ where: { id: supplierId, tenantId: req.tenantId } });
    if (!supplier) return res.status(404).json({ error: 'Supplier not found' });

    const count = await prisma.purchaseOrder.count({ where: { tenantId: req.tenantId } });
    const poNumber = generatePONumber('PO', count + 1);

    let subtotal = 0, vatAmount = 0;
    const processedLines = lines.map((l) => {
      const lineSubtotal = roundDecimal(l.quantity * l.unitPrice);
      const vat = calculateVAT(lineSubtotal, l.vatRate ?? 0.075);
      subtotal += lineSubtotal;
      vatAmount += vat;
      return { ...l, vatRate: l.vatRate ?? 0.075, vatAmount: vat, lineTotal: roundDecimal(lineSubtotal + vat) };
    });

    const po = await prisma.purchaseOrder.create({
      data: {
        tenantId: req.tenantId, supplierId, poNumber,
        issueDate: new Date(),
        expectedDate: expectedDate ? new Date(expectedDate) : null,
        currency: currency || 'NGN',
        exchangeRate: exchangeRate || 1,
        subtotal: roundDecimal(subtotal),
        vatAmount: roundDecimal(vatAmount),
        whtAmount: 0,
        totalAmount: roundDecimal(subtotal + vatAmount),
        notes, createdById: req.user.id,
        lines: { create: processedLines },
      },
      include: { supplier: true, lines: true },
    });
    await createAuditLog({ tenantId: req.tenantId, userId: req.user.id, action: 'CREATE', resource: 'PurchaseOrder', resourceId: po.id, req });
    res.status(201).json({ data: po });
  } catch (e) { res.status(500).json({ error: 'Failed to create purchase order' }); }
});

