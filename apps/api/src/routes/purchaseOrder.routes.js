router.get('/', async (req, res) => {
  try {
    const { page, limit, status, supplierId, isAutoGenerated } = req.query;
    const { take, skip } = paginate(page, limit);
    const where = { tenantId: req.tenantId };
    if (status) where.status = status;
    if (supplierId) where.supplierId = supplierId;
    if (isAutoGenerated !== undefined) where.isAutoGenerated = isAutoGenerated === 'true';
    const [data, total] = await Promise.all([
      prisma.purchaseOrder.findMany({
        where, take, skip, orderBy: { createdAt: 'desc' },
        include: { supplier: { select: { id: true, name: true } }, lines: { include: { product: { select: { id: true, name: true, sku: true } } } } },
      }),
      prisma.purchaseOrder.count({ where }),
    ]);
    res.json(paginatedResponse(data, total, page, limit));
  } catch (e) { res.status(500).json({ error: 'Failed to fetch purchase orders' }); }
});

